public class PrimitiveApp {

    public static void main(String[] args) {
        long number = 1000;
        System.out.println(number * number);
        System.out.println(1000 * 1000); //number Literal
        System.out.println(number * number * number);
        System.out.println(number * number * number * number * number * number * number * number * number * number * number);

        double balance = 13.79;
        System.out.println(balance);
    }
}